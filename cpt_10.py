# while: utilizzo while quando voglio che un pezzo di codico se repeta indefiniditamente

conteggio = 0

while conteggio < 5:
    print(conteggio)
    conteggio += 1


# for: viene utilizzata per interare su una sequenza di elementi (come una lista, una stringa o una tupla), qui lo so quante volte voglio ripetere

numeri = [1, 2, 3, 4, 5]

for numero in numeri: #numero Ã¨ solo un riferimento, una variaile creata solo per chiamar per il print
    print(numero)

